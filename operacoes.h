/*
\brief A função soma executa a soma de dois valores inteiros, recebidos por parâmetro.
\param x Primeiro inteiro.
\param y Segundo inteiro.
\return Retorna o resultado da soma do primeiro com o segundo inteiro.
*/
int soma(int x, int y);

/*
\brief A função soma executa a multiplicação de dois valores inteiros, recebidos por parâmetro.
\param x Primeiro inteiro.
\param y Segundo inteiro.
\return Retorna o resultado da multiplicação do primeiro com o segundo inteiro.
*/
int multiplica(int x, int y);

/*
\brief A função soma executa a subtração de dois valores inteiros, recebidos por parâmetro.
\param x Primeiro inteiro.
\param y Segundo inteiro.
\return Retorna o resultado da subtração do primeiro com o segundo inteiro.
*/
int subtrai(int x, int y);

/*
\brief A função soma executa a divisão de dois valores inteiros, recebidos por parâmetro.
\param x Primeiro inteiro.
\param y Segundo inteiro.
\return Retorna o resultado da divisão do primeiro com o segundo inteiro.
*/
int divide(int x, int y);